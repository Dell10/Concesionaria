package Modelo;
public class ModeloEmpleados {
    private String cveElectorEmpleado;
    private String nombreEmpleado;
    private String apPatEmpleado;
    private String apMatEmpleado;
    private String direccionEmpleado;
    private String telefonoEmpleado;
    private String puestoEmpleado;
    private double salarioEmpleado;
    private boolean estadoEmpleado;
    public String getCveElectorEmpleado() {
        return cveElectorEmpleado;
    }
    public void setCveElectorEmpleado(String cveElectorEmpleado) {
        this.cveElectorEmpleado = cveElectorEmpleado;
    }
    public String getNombreEmpleado() {
        return nombreEmpleado;
    }
    public void setNombreEmpleado(String nombreEmpleado) {
        this.nombreEmpleado = nombreEmpleado;
    }
    public String getApPatEmpleado() {
        return apPatEmpleado;
    }
    public void setApPatEmpleado(String apPatEmpleado) {
        this.apPatEmpleado = apPatEmpleado;
    }
    public String getApMatEmpleado() {
        return apMatEmpleado;
    }
    public void setApMatEmpleado(String apMatEmpleado) {
        this.apMatEmpleado = apMatEmpleado;
    }
    public String getDireccionEmpleado() {
        return direccionEmpleado;
    }
    public void setDireccionEmpleado(String direccionEmpleado) {
        this.direccionEmpleado = direccionEmpleado;
    }
    public String getTelefonoEmpleado() {
        return telefonoEmpleado;
    }
    public void setTelefonoEmpleado(String telefonoEmpleado) {
        this.telefonoEmpleado = telefonoEmpleado;
    }
    public String getPuestoEmpleado() {
        return puestoEmpleado;
    }
    public void setPuestoEmpleado(String puestoEmpleado) {
        this.puestoEmpleado = puestoEmpleado;
    }
    public double getSalarioEmpleado() {
        return salarioEmpleado;
    }
    public void setSalarioEmpleado(double salarioEmpleado) {
        this.salarioEmpleado = salarioEmpleado;
    }
    public boolean isEstadoEmpleado() {
        return estadoEmpleado;
    }
    public void setEstadoEmpleado(boolean estadoEmpleado) {
        this.estadoEmpleado = estadoEmpleado;
    }
}